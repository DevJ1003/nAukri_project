/*************************DELETE JOB *****************************/

$(document).ready(function () {

    $(".job_delete").click(function () {

        var user_input;

        location.reload();
        return user_input = confirm("Are you sure you want to delete this job ?");

    });









    $(".job_apply").click(function () {

        var user_input;

        location.reload();
        return user_input = confirm("Do you want to apply for this job ?");

    });



    $(".job_retrieve").click(function () {

        var user_input;

        location.reload();
        return user_input = confirm("Are you sure you want to retrieve your job application ?");

    });





});